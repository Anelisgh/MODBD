-- DIMENSIUNEA PASAGER
CREATE TABLE DIM_PASAGER (
    id_dim_pasager NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY
         CONSTRAINT pk_dim_pasager PRIMARY KEY,
    id_pasager_oltp NUMBER(10) NOT NULL
         CONSTRAINT uk_dim_pasager_oltp UNIQUE DISABLE VALIDATE,
    nationalitate VARCHAR2(3),
    varsta NUMBER(3),
    grupa_varsta VARCHAR2(20),
    CONSTRAINT chk_dim_pasager_varsta CHECK (varsta BETWEEN 0 AND 120)
);

-- DIMENSIUNEA UTILIZATOR
CREATE TABLE DIM_UTILIZATOR (
    id_dim_utilizator NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY
         CONSTRAINT pk_dim_utilizator PRIMARY KEY,
    id_user_oltp NUMBER(10) NOT NULL
         CONSTRAINT uk_dim_utilizator_oltp UNIQUE DISABLE VALIDATE,
    vechime_cont_ani NUMBER(3)
);

-- TABEL FACT - FACT_VANZARI
CREATE TABLE FACT_VANZARI (
    id_vanzare NUMBER(15) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    
    id_dim_timp_rezervare NUMBER(10) NOT NULL
         CONSTRAINT fk_fact_timp_rez
         REFERENCES DIM_TIMP(id_dim_timp)
         RELY DISABLE NOVALIDATE, -- nu verifica corectitudinea datelor la insert (am facut asta deja in etl), nu verifica nici datele existente, "promitem" ca sunt corecte (pentru optimizare)
    id_dim_timp_plecare NUMBER(10) NOT NULL
         CONSTRAINT fk_fact_timp_plec
         REFERENCES DIM_TIMP(id_dim_timp)
         RELY DISABLE NOVALIDATE,
    id_dim_aeroport_plecare NUMBER(10) NOT NULL
         CONSTRAINT fk_fact_aero_plec
         REFERENCES DIM_AEROPORT(id_dim_aeroport)
         RELY DISABLE NOVALIDATE,
    id_dim_aeroport_sosire NUMBER(10) NOT NULL
         CONSTRAINT fk_fact_aero_sos
         REFERENCES DIM_AEROPORT(id_dim_aeroport)
         RELY DISABLE NOVALIDATE,
    id_dim_avion NUMBER(10) NOT NULL
         CONSTRAINT fk_fact_avion
         REFERENCES DIM_AVION(id_dim_avion)
         RELY DISABLE NOVALIDATE,
    id_dim_pasager NUMBER(10) NOT NULL
         CONSTRAINT fk_fact_pasager
         REFERENCES DIM_PASAGER(id_dim_pasager)
         RELY DISABLE NOVALIDATE,
    id_dim_clasa NUMBER(10) NOT NULL
         CONSTRAINT fk_fact_clasa
         REFERENCES DIM_CLASA(id_dim_clasa)
         RELY DISABLE NOVALIDATE,
    id_dim_utilizator NUMBER(10) NOT NULL
         CONSTRAINT fk_fact_utilizator
         REFERENCES DIM_UTILIZATOR(id_dim_utilizator)
         RELY DISABLE NOVALIDATE,
         
    data_plecare_part DATE NOT NULL,
    pret_bilet NUMBER(10,2) NOT NULL,
    durata_zbor NUMBER(8,2),
    minute_intarziere NUMBER(8,2) DEFAULT 0,
    status_zbor VARCHAR2(20),
    
    CONSTRAINT pk_fact_vanzari PRIMARY KEY (id_vanzare),
    CONSTRAINT chk_fact_pret CHECK (pret_bilet >= 0),
    CONSTRAINT chk_fact_durata CHECK (durata_zbor >= 0),
    CONSTRAINT chk_fact_status CHECK (status_zbor IN ('PROGRAMAT','ANULAT','INTARZIAT','IMBARCAT','IN_ZBOR','ATERIZAT'))
)
PARTITION BY RANGE (data_plecare_part) (
    PARTITION p_2023 VALUES LESS THAN (DATE '2024-01-01'),
    PARTITION p_2024 VALUES LESS THAN (DATE '2025-01-01'),
    PARTITION p_2025 VALUES LESS THAN (DATE '2026-01-01'),
    PARTITION p_future VALUES LESS THAN (MAXVALUE)
);

