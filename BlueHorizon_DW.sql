-- CREATE TABLES
-- DIMENSIUNEA TIMP
CREATE TABLE DIM_TIMP (
    id_dim_timp NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY
         CONSTRAINT pk_dim_timp PRIMARY KEY,
    data_calendaristica DATE NOT NULL
         CONSTRAINT uk_dim_timp_data UNIQUE DISABLE VALIDATE, -- verifica datele existente, nu face index (pt a economisi spatiul), dar blocheaza orice insert (devine read-only)
                                                            -- in viitorul proces etl va trebui sa alteram coloana cu DISABLE NOVALIDATE,
                                                            -- apoi vor fi inserate datele si vom resigila coloana printr-un alt alter pe coloana cu DISABLE VALIDATE
    an NUMBER(4) NOT NULL,
    trimestru NUMBER(1) NOT NULL,
    nume_luna VARCHAR2(20),
    luna NUMBER(2) NOT NULL,
    nume_zi VARCHAR2(20),
    zi NUMBER(2) NOT NULL,
    CONSTRAINT chk_dim_timp_trimestru CHECK (trimestru BETWEEN 1 AND 4),
    CONSTRAINT chk_dim_timp_luna CHECK (luna BETWEEN 1 AND 12),
    CONSTRAINT chk_dim_timp_zi CHECK (zi BETWEEN 1 AND 31)
);

-- DIMENSIUNEA AEROPORT
CREATE TABLE DIM_AEROPORT (
    id_dim_aeroport NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY
         CONSTRAINT pk_dim_aeroport PRIMARY KEY,
    id_aeroport_oltp NUMBER(10) NOT NULL
         CONSTRAINT uk_dim_aeroport_oltp UNIQUE DISABLE VALIDATE,
    cod_iata CHAR(3) NOT NULL
         CONSTRAINT uk_dim_aeroport_iata UNIQUE DISABLE VALIDATE,
    nume_aeroport VARCHAR2(200) NOT NULL,
    oras VARCHAR2(100) NOT NULL,
    tara VARCHAR2(100) NOT NULL
);

-- DIMENSIUNEA AVION
CREATE TABLE DIM_AVION (
    id_dim_avion NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY
         CONSTRAINT pk_dim_avion PRIMARY KEY,
    id_avion_oltp NUMBER(10) NOT NULL
         CONSTRAINT uk_dim_avion_oltp UNIQUE DISABLE VALIDATE,
    numar_inmatriculare VARCHAR2(20) NOT NULL
         CONSTRAINT uk_dim_avion_nr UNIQUE DISABLE VALIDATE,
    model VARCHAR2(100) NOT NULL,
    capacitate NUMBER(5) NOT NULL,
    an_fabricatie NUMBER(4),
    CONSTRAINT chk_dim_avion_capacitate CHECK (capacitate > 0)
);

-- DIMENSIUNEA CLASA
